    private void validarRegistro() throws ExcepcionRegistro, SQLException {
        if (txtNombreInfo.getText().equals(""))
            throw new ExcepcionRegistro("Escriba un nombre");
        if (txtAPInfo.getText().equals(""))
            throw new ExcepcionRegistro("Escriba su apellido");
        if (txtFechaNacInfo.getText().length() != 10)
            throw new ExcepcionRegistro("Fecha de nacimiento invalida");
        if (txtDireccionInfo.getText().equals(""))
            throw new ExcepcionRegistro("Escriba una direccion valida");
        if (txtCorreoInfo.getText().equals(""))
            throw new ExcepcionRegistro("Escriba una direccion de correo valida");
        if (fabricaDAO.getSocioDAO().existeCorreo(txtCorreoInfo.getText()) != 0)
            throw new ExcepcionRegistro("Esta direccion de correo ya esta ligada a otro socio");
        if (txtTelefonoInfo.getText().equals(""))
            throw new ExcepcionRegistro("Escriba un telefono valido");
        if (txtContrasenaInfo.getText().equals(""))
            throw new ExcepcionRegistro("Escriba una contrasena");
        if (!txtContrasenaInfo.getText().equals(txtRepContrasenaInfo.getText()))
            throw new ExcepcionRegistro("Las contrasenas no coinciden");
        if (txtContrasenaInfo.getText().length() < 8)
            throw new ExcepcionRegistro("La contrasena es muy corta");
        if ((!radioInfo.isSelected()) && (!radioMInfo.isSelected()))
            throw new ExcepcionRegistro("Seleccione su sexo");
        if (cboxPaisInfo.getSelectedIndex() == 0)
            throw new ExcepcionRegistro("Seleccione un pais");
        if (cboxEstadoInfo.getSelectedIndex() == 0)
            throw new ExcepcionRegistro("Seleccione un estado");
    }
    
    private void guardarRegistro(){
        socioRegistro.setNombre(txtNombreInfo.getText());
        socioRegistro.setApellidoPaterno(txtAPInfo.getText());
        socioRegistro.setApellidoMaterno(txtAMInfo.getText());
        socioRegistro.setNombreCompleto();
        socioRegistro.setFechaNacimiento(txtFechaNacInfo.getText());
        socioRegistro.setDireccion(txtDireccionInfo.getText());
        socioRegistro.setCorreo(txtCorreoInfo.getText());
        socioRegistro.setPassword(txtContrasenaInfo.getText());
        socioRegistro.setTelefono(txtTelefonoInfo.getText());
        if (radioInfo.isSelected())
            socioRegistro.setSexo("F");
        else
            socioRegistro.setSexo("M");
        int index = cboxEstadoInfo.getSelectedIndex() - 1;
        socioRegistro.setIdEstado(listaEstados.get(index).getId());
    }
    
