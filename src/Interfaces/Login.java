    private void btnConectarLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConectarLoginActionPerformed
        try {
            conectarMySQL();
            String user = txtUsuario.getText();
            String password = txtContrasenaLogin.getText();
            if (!user.equals(password)) {
                ObjetoSocio socio = fabricaDAO.getSocioDAO().login(Integer.parseInt(user), password);
                if (socio != null){
                    Administrador.correr(socio, fabricaDAO, conexion);
                    this.setVisible(false);
                } else
                    notificaciónLogin.setText("El numero de socio no existe o no se ha dado de alta");
            } else
                notificaciónLogin.setText("Numero de socio y/o contraseña incorrecta");
        } catch (SQLException e) {
            notificaciónLogin.setText("Error de conexion");
            int confirmacion = JOptionPane.showConfirmDialog(this, "Hubo un error al realizar la conexion.\n¿Desea acceder de manera local?",
                    "Error de conexion", JOptionPane.YES_NO_OPTION);
            if (confirmacion == JOptionPane.YES_OPTION) {
                try {
                    conectarHSQL();
                    crearTablasHSQL();
                    String user = txtUsuario.getText();
                    String password = txtContrasenaLogin.getText();
                    if (!user.equals(password)) {
                        ObjetoSocio socio = fabricaDAO.getSocioDAO().login(Integer.parseInt(user), password);
                        if (socio != null) {
                            Administrador admin = new Administrador(socio, fabricaDAO, conexion);
                            admin.setVisible(true);
                            admin.setAdminVisible(false);
                            admin.setDescargarEneable(false);
                            this.setVisible(false);
                        } else {
                            notificaciónLogin.setText("El numero de socio no existe o no se ha dado de alta");
                        }
                    } else {
                        notificaciónLogin.setText("Numero de socio y/o contraseña incorrecta");
                    }
                } catch (SQLException ex) {
                    System.out.println("error de conexion en HSQL");
                    e.printStackTrace();
                } catch (ClassNotFoundException ex) {
                    notificaciónLogin.setText("Error en la conexion: No Driver");
                } catch (NumberFormatException ex) {
                    notificaciónLogin.setText("Numero de socio invalido");
                }
            }
        } catch (NumberFormatException e){
            notificaciónLogin.setText("Numero de socio invalido");
        } catch (ClassNotFoundException e){
            notificaciónLogin.setText("Error en la conexion: No Driver");
        }
    }//GEN-LAST:event_btnConectarLoginActionPerformed

