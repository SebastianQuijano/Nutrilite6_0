    private void btnEnviarRecuperarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnviarRecuperarActionPerformed
        
        try {
            conectarMySQL();
            String correoDestino = txtCorreoRecuperar.getText();
            String password = fabricaDAO.getSocioDAO().verContraseña(correoDestino);
            if (password == null) 
                JOptionPane.showMessageDialog(this, "Correo electronico no valido");
            else {
                EnviarCorreo.sendMessage(correoDestino, password);
                JOptionPane.showMessageDialog(this, "Su contraseña ha sido enviada");
                panelConectarme.setVisible(true);
                panelRecuperarContrasena.setVisible(false);
                setSize(570, 360);
                setLocationRelativeTo(null);
            }
        } catch (ClassNotFoundException e) {
            JOptionPane.showMessageDialog(this, "Error de conexion: No Driver");
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error de conexion");
        } catch (MessagingException e) {
            JOptionPane.showMessageDialog(this, "No ha sido posible enviar el correo");
            e.printStackTrace();
        }
        
    }//GEN-LAST:event_btnEnviarRecuperarActionPerformed

